def analyze_weaknesses(client, wrong_answers):

    weak_topics = ""

    for wrong in wrong_answers:
        weak_topics += f"""
Модуль:
{wrong['module']}

Вопрос:
{wrong['question']}

Неправильный ответ:
{wrong['user_answer']}

Правильный ответ:
{wrong['correct_answer']}
"""

    response = client.responses.create(
        model="gpt-4.1-mini",
        input=f"""
Стажёр прошёл тест и допустил ошибки.

Проанализируй ошибки стажёра.

ВАЖНО:
- Определи, в каких модулях стажёр ошибается чаще всего.
- Найди темы, которые он понял хуже всего.
- Основывайся только на предоставленных ошибках.
- Не используй внешние знания.

Верни:
1. Самые слабые модули стажёра
2. Какие темы необходимо повторить
3. Какие ошибки повторяются чаще всего
4. 3 рекомендации по обучению
5. Одно дополнительное практическое задание

Ошибки стажёра:

{weak_topics}
"""
    )

    return response.output_text