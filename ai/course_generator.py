import json


def generate_course(client, file_content):
    response = client.responses.create(
        model="gpt-4.1-mini",
        text={
            "format": {
                "type": "json_object"
            }
        },
        input=f"""
Ты — профессиональный AI-тренер корпоративного обучения.

Твоя задача:
создать ПОЛНОЦЕННЫЙ обучающий курс СТРОГО на основе предоставленного материала.

ВАЖНО:
- НЕ делай краткий пересказ
- НЕ сокращай информацию
- НЕ создавай summary
- Объясняй тему подробно
- Пиши так, будто обучаешь новичка с нуля
- Каждый модуль должен раскрывать тему глубоко и понятно
- Используй примеры
- Используй пошаговые объяснения
- Добавляй практические советы
- Делай обучение приближенным к реальной работе

Если материал большой:
- создай больше модулей
- разделяй темы логически

СТРОГО ЗАПРЕЩЕНО:
- придумывать темы, которых нет в материале
- добавлять CRM, клиентов или техподдержку, если этого нет
- использовать внешние знания
- сокращать объяснения

ТРЕБОВАНИЯ К МОДУЛЯМ:
- минимум 1500 символов на каждый модуль
- каждый модуль должен быть полноценным обучающим блоком
- модуль должен объяснять:
  - что это
  - зачем это нужно
  - как это работает
  - как применять на практике
  - типичные ошибки
  - реальные примеры

ТРЕБОВАНИЯ К ТЕСТАМ:
- вопросы должны проверять понимание материала
- избегай слишком простых вопросов
- варианты ответов должны быть реалистичными

ТРЕБОВАНИЯ К ПРАКТИКЕ:
- практическое задание должно быть максимально приближено к реальной задаче

Верни JSON строго по структуре:

{{
    "course_title": "Название курса",
    "modules": [
        {{
            "title": "Название модуля",
            "description": "Краткое описание",
            "content": "Подробный обучающий материал"
        }}
    ],
    "test": [
        {{
            "question": "Вопрос",
            "options": ["1", "2", "3", "4"],
            "correct_answer": "Правильный ответ"
        }}
    ],
    "practical_task": "Практическое задание"
}}

Материалы компании:
{file_content}
"""
    )

    raw_text = response.output_text
    course_data = json.loads(raw_text)

    return course_data